public class LogicoE {
    //Método principal da classe//
    public static void main(String[] args){
        boolean a = true;
        boolean b = false;
        boolean c = true;
        //Operador lógico &&(e) só será verdadeiro se ambos operadores forem verdadeiros 
        System.out.println(a && b);
        System.out.println(a && c);
    }
    
}
