public class LogicoOU {
    //Método principal da classe
    public static void main(String[] args){
        boolean a = true;
        boolean b = false;
        boolean c = true;
        //Operador lógico ||(ou) será verdadeiro se ao menos um dos operadores forem veradeiros
        System.out.println(a || b);
        System.out.println(a || c);
    }
}
