//Estrutura de repetição FAÇA...ENQUANTO
public class ControledeFluxoDOWHILE {
    public static void main(String [] args){ //Fluxo de repetição usando While
        int i = 0; //A variável recebe o valor zero (0).
        do {
            System.out.println(i); //Imprime o valor de i i++;
            i++;
        } while (i < 10);
    }

}
