//Estrutura de Repetição ENQUANTO
public class ControledeFluxoWHILE { 
    public static void main(String [] args){
        //Fluxo de Repetição usando While
        int i = 0; //A variável i recebe o valor zero
        while (i <= 2) { //Enquanto i e menor ou igual do que 2 
            System.out.println(i); //Imprime o valor de i
            i++;
        }
    }
}
